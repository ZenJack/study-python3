# 数据类型：整数、浮点数、字符串、布尔值、空值
# 1. 整数
print(1)
# 2. 浮点数
print(1.0)
# 3. 字符串
print('Hello World!')
# 多行字符串
print('''
Hello 
World
!
''')
# 4. 布尔值 True、False 使用 and、or、not 连接
print(True)
print(False)
print(True and False)
print(True or False)
print(not True)
# 5. 空值 None
print(None)
# 变量、常量
# 1. 变量
a = 1
print(a)
a = 'abc'
print(a)
# 2. 常量，约定变量名全部大写为常量
PI = 3.1415926535
print(PI)
